app.get("/login", (req, res) => {
  // Check for existing cookie (user logged in?)
  const u_id = req.cookies.user_id;
  if (u_id) {
    res.redirect("/urls");
  }
  // Render login page if user not logged in
  const templateVars = {
    user: users[req.cookies.user_id]
  };
  res.render("login", templateVars);
});

app.post("/login", (req, res) => {
  const email = req.body.email;
  const password = req.body.password;
  // Is email already registered?
  let foundUser = null;
  for (const u_id in users) {
    const user = users[u_id];
    if (user.email === email) {
      foundUser = user;
    }
  }
  // If email is not registered:
  if (!foundUser) {
    return res.status(403).send("Email is not registered.");
  }
  // If password does not match:
  if (foundUser.password !== password) {
    return res.status(403).send("Password is incorrect.");
  }

  res.cookie("user_id", foundUser.id);
  res.redirect("/urls");
});

app.post("/logout", (req, res) => {
  res.cookie("user_id", users[req.cookies.user_id]);
  res.clearCookie("user_id", users[req.cookies.user_id]);
  res.redirect("/login");
});

