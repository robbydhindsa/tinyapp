const cookieParser = require("cookie-parser");
const express = require("express");
const app = express();
const PORT = 8080; // default port 8080

function generateRandomString() {
  let result = Math.random().toString(36).substring(2, 8);
  return result;
}

app.set("view engine", "ejs");
app.use(express.urlencoded({ extended: true })); // populates req.body - express's "body parser"
app.use(cookieParser()); // populates req.cookies

const urlDatabase = {
  "b2xVn2": "http://www.lighthouselabs.ca",
  "9sm5xK": "http://www.google.com"
};

const users = {
  abc: {
    id: "abc",
    email: "a@a.com",
    password: "1234"
  },
  def: {
    id: "def",
    email: "b@b.com",
    password: "5678"
  }
};

app.listen(PORT, () => {
  console.log(`Example app listening on port ${PORT}!`);
});


app.get("/urls", (req, res) => {
  const templateVars = {
    urls: urlDatabase,
    user: users[req.cookies.user_id]
  };
  res.render("urls_index", templateVars);
});


app.get("/urls/new", (req, res) => {
  // If user not logged in, redirect to login page
  const u_id = req.cookies.user_id;
  if (!u_id) {
    res.redirect("/login");
  }
  // If user logged in, render urls_new template
  const templateVars = {
    user: users[req.cookies.user_id]
  };
  res.render("urls_new", templateVars);
});

